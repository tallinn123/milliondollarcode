package lesson2_concat_converting_scanner;

public class Concatenation {

    /* Combining strings is called concatenation.
    The following statement combines the value of two string variables to create a third string:
     */

    public static void main(String[] args) {

        String one = "Hello, ";
        String two = "World!";
        String greeting = one + two; // Combine two strings by using the plus sign (+) as a concatenation operator.

        System.out.println(greeting);  //Hello, World!

    }
}
